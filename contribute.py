def cmd_stage(base, src_file, mark_translated=False):
    text_dir    = os.path.join(base, TEXT_DIR)
    texture_dir = os.path.join(base, TEXTURE_DIR)
    staging_dir = os.path.join(base, STAGING_DIR)

    # Textures first
    print('=== Textures ===')
    moved = organise_textures(texture_dir, mark_translated=mark_translated, base=base)
    if moved:
        default_label = '[translated]' if mark_translated else '[untranslated]'
        for f, action in moved:
            label = f'[{action}]' if action == 'replaced' else default_label
            print(f'  {label}  {f}')
        print(f'  {len(moved)} file(s) organised.')
    else:
        print('  Nothing to organise.')

    # Strings
    print(f'\n=== Strings: {src_file} ===')
    if not os.path.isfile(src_file):
        print(f'ERROR: {src_file} not found.')
        sys.exit(1)

    existing_keys = load_existing_keys(text_dir)
    # Also include anything already in staging so re-runs don't duplicate
    if os.path.isdir(staging_dir):
        existing_keys |= load_existing_keys(staging_dir)

    print(f'  {len(existing_keys)} existing keys (translated + staged).')

    with open(src_file, 'r', encoding='utf-8-sig') as f:
        incoming = f.readlines()

    buckets      = {}
    skipped_dup  = 0
    skipped_bad  = 0

    for raw in incoming:
        line = raw.rstrip('\n\r')
        if not line or line.startswith('//') or line.startswith('#'):
            continue
        if '=' not in line:
            skipped_bad += 1
            continue
        jp_key = line.split('=', 1)[0]
        if jp_key in existing_keys:
            skipped_dup += 1
            continue
        cat = classify(line)
        buckets.setdefault(cat, []).append(raw if raw.endswith('\n') else raw + '\n')
        existing_keys.add(jp_key)

    if not buckets:
        print('  No new strings found.')
        return

    os.makedirs(staging_dir, exist_ok=True)
    added_total = 0
    for cat, lines in sorted(buckets.items()):
        dest_file = os.path.join(staging_dir, f'{cat}.txt')
        # Append to staging file (supports multiple runs)
        with open(dest_file, 'a', encoding='utf-8') as f:
            f.writelines(lines)
        print(f'  staged {len(lines):>4}  ->  staging/{cat}.txt')
        added_total += len(lines)

    print(f'\n  New strings staged : {added_total}')
    print(f'  Skipped (duplicate): {skipped_dup}')
    if skipped_bad:
        print(f'  Skipped (no "=")   : {skipped_bad}')
    print(f'\nReview staging/ then run:  py contribute.py --apply')


# ---------------------------------------------------------------------------
# Apply command
# ---------------------------------------------------------------------------

def cmd_apply(base):
    text_dir    = os.path.join(base, TEXT_DIR)
    staging_dir = os.path.join(base, STAGING_DIR)

    if not os.path.isdir(staging_dir) or not os.listdir(staging_dir):
        print('Nothing in staging/ to apply.')
        return

    print('=== Applying staging -> translation files ===')
    applied_total = 0
    for fname in sorted(os.listdir(staging_dir)):
        if not fname.endswith('.txt'):
            continue
        src  = os.path.join(staging_dir, fname)
        dest = os.path.join(text_dir, fname)
        with open(src, 'r', encoding='utf-8') as f:
            lines = f.readlines()
        with open(dest, 'a', encoding='utf-8') as f:
            f.writelines(lines)
        os.remove(src)
        print(f'  applied {len(lines):>4}  ->  {fname}')
        applied_total += len(lines)

    # Remove staging dir if empty
    if not os.listdir(staging_dir):
        os.rmdir(staging_dir)

    print(f'\n  Total applied: {applied_total}')
    print('  staging/ cleared.')


