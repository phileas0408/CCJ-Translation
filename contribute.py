def classify(s):
    if not s or '=' not in s:
        return None
    for kw in SYSTEM_KW:
        if kw in s: return 'system'
    for kw in TUTORIAL_KW:
        if kw in s: return 'tutorial'
    for kw in MUSIC_KW:
        if kw in s: return 'music'
    for kw in CHARACTER_KW:
        if kw in s: return 'characters'
    if 'color\\=#dc3096>' in s: return 'skills'
    if 'color\\=#18bcf2>' in s: return 'abilities'
    for kw in TITLE_KW:
        if kw in s: return 'titles'
    if TITLE_COLOR.search(s): return 'titles'
    for kw in COSMETIC_KW:
        if kw in s: return 'cosmetics'
    for kw in MISSION_KW:
        if kw in s: return 'missions'
    return 'gameplay'


