def load_existing_keys(text_dir):
    keys = set()
    for fname in os.listdir(text_dir):
        if not fname.endswith('.txt') or fname.startswith('_'):
            continue
        with open(os.path.join(text_dir, fname), 'r', encoding='utf-8-sig') as f:
            for line in f:
                line = line.strip()
                if '=' in line:
                    keys.add(line.split('=', 1)[0])
    return keys


SCENE_FOLDERS = {
    'card_entry', 'character_setting', 'chat', 'common', 'gameover',
    'gameplay', 'matching', 'mode_select', 'result', 'sakasamatower',
    'shop', 'stage', 'tutorial',
}


def get_scene(fname):
    m = re.match(r'sactx-\d+-\d+x\d+-\w+-([a-z_]+)-', fname)
    if m:
        return m.group(1)
    if fname.startswith('matching_rank_'):
        return 'matching'
    if fname.startswith('ui_chat_') or fname.startswith('ui_emote'):
        return 'chat'
    if fname.startswith('ui_stage_name_'):
        return 'stage'
    if fname.startswith('body_') or fname.startswith('headacc_') or fname.startswith('shoes_') or fname.startswith('weapon_') or fname.startswith('other_'):
        return 'item_dressup'
    if fname.startswith('ui_consume') or fname.startswith('ui_dress'):
        return 'item_obtain'
    if fname.startswith('ui_information_'):
        return 'information'

    return None


def sort_loose_files(scan_dir, dest_prefix, texture_dir, moved):
    """Sort any loose .png/.psd files in scan_dir into dest_prefix/<scene>/ subfolders."""
    for fname in os.listdir(scan_dir):
        fpath = os.path.join(scan_dir, fname)
        if os.path.isdir(fpath):
            continue
        if not (fname.endswith('.png') or fname.endswith('.psd')):
            continue
        scene = get_scene(fname)
        if scene is None:
            if not (scan_dir.endswith('ignored') or scan_dir.endswith('need_sorting')):
                print(f'  [texture] WARNING: no scene rule for "{fname}" — left in place')
                continue
            continue
        dest_dir = os.path.join(texture_dir, dest_prefix, scene)
        os.makedirs(dest_dir, exist_ok=True)
        dest_path = os.path.join(dest_dir, fname)
        replacing = os.path.isfile(dest_path)
        if replacing:
            os.remove(dest_path)
        shutil.move(fpath, dest_path)
        action = 'replaced' if replacing else 'moved'
        moved.append((dest_prefix, scene, fname, action))


def find_in_untranslated(texture_dir, fname):
    """Return all paths under _untranslated/ that match fname."""
    untranslated_dir = os.path.join(texture_dir, '_untranslated')
    matches = []
    if not os.path.isdir(untranslated_dir):
        return matches
    for dirpath, _, files in os.walk(untranslated_dir):
        if fname in files:
            matches.append(os.path.join(dirpath, fname))
    return matches


def check_duplicates(texture_dir):
    """Warn about textures that appear in more than one location under Texture/."""
    seen = {}  # fname -> list of paths
    for dirpath, _, files in os.walk(texture_dir):
        for fname in files:
            if fname.endswith('.png') or fname.endswith('.psd'):
                seen.setdefault(fname, []).append(
                    os.path.relpath(os.path.join(dirpath, fname), texture_dir).replace('\\', '/')
                )
    duplicates = {f: paths for f, paths in seen.items() if len(paths) > 1}
    if duplicates:
        print(f'  WARNING: {len(duplicates)} duplicate filename(s) found:')
        for fname, paths in sorted(duplicates.items()):
            for p in paths:
                print(f'    {p}')
    return duplicates


def organise_textures(texture_dir, mark_translated=False, base=None):
    """Sort loose texture files into scene subfolders.

    - Texture/ root         -> Texture/<scene>/ (--translated)
                               Texture/_untranslated/<scene>/ (default)
    - _untranslated/ root   -> _untranslated/<scene>/
    - _untranslated/<cat>/  -> _untranslated/<cat>/<scene>/  (contributor-created folders)

    With --translated: removes any copies found under _untranslated/.
    Always checks for duplicate filenames across all subfolders.
    If mark_translated=True, .png paths are registered in translated_textures.json.
    """
    moved = []
    untranslated_dir = os.path.join(texture_dir, '_untranslated')

    # Sort files dropped in Texture/ root
    root_prefix = '' if mark_translated else '_untranslated'
    sort_loose_files(texture_dir, root_prefix, texture_dir, moved)

    # Sort files dropped in _untranslated/ root and any contributor subfolders within it
    if os.path.isdir(untranslated_dir):
        sort_loose_files(untranslated_dir, '_untranslated', texture_dir, moved)
        for entry in sorted(os.listdir(untranslated_dir)):
            entry_path = os.path.join(untranslated_dir, entry)
            if os.path.isdir(entry_path) and entry.lower() not in SCENE_FOLDERS:
                sort_loose_files(entry_path, os.path.join('_untranslated', entry), texture_dir, moved)

    # If translating: remove any copies of the same file from _untranslated/
    if mark_translated and moved:
        for _, _, fname, _ in moved:
            stale = find_in_untranslated(texture_dir, fname)
            for path in stale:
                os.remove(path)
                rel = os.path.relpath(path, texture_dir).replace('\\', '/')
                print(f'  removed from _untranslated/  {rel}')

    # Check for duplicates across all subfolders
    print('  Checking for duplicates...')
    dupes = check_duplicates(texture_dir)
    if not dupes:
        print('  No duplicates found.')

    if mark_translated and moved and base:
        manifest_path = os.path.join(base, MANIFEST_FILE)
        existing = []
        if os.path.isfile(manifest_path):
            with open(manifest_path, 'r', encoding='utf-8') as f:
                existing = json.load(f)
        existing_set = set(existing)
        new_entries = []
        for prefix, scene, fname, _replacing in moved:
            if fname.endswith('.png'):
                rel = f'{TEXTURE_DIR}/{prefix}/{scene}/{fname}'.replace('\\', '/').replace('//', '/')
                if rel not in existing_set:
                    new_entries.append(rel)
        if new_entries:
            updated = sorted(existing + new_entries)
            with open(manifest_path, 'w', encoding='utf-8') as f:
                json.dump(updated, f, indent=2, ensure_ascii=False)
                f.write('\n')
            print(f'  Added {len(new_entries)} texture(s) to {MANIFEST_FILE}')

    return [(f'{os.path.join(prefix, scene)}/{fname}', action) for prefix, scene, fname, action in moved]


